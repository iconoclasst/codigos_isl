import numpy as np
import pandas as pd
from matplotlib.pyplot import subplots

url='https://www.statlearning.com/s/Auto.csv'
Auto = pd.read_csv(url, na_values='?')
Auto_n = Auto.dropna()
Auto=Auto_n

Auto.dtypes
