#Quantitativas: resto

r1 = np.max(Auto['mpg'] - np.min(Auto['mpg']))
r1

# Range de cada coluna quantitativa
ranges = {}
for col in Auto.select_dtypes(include=['float64', 'int64']):
  ranges[col] = np.max(Auto[col]) - np.min(Auto[col])
ranges

# Média de cada coluna quantitativa
means = {}
for col in Auto.select_dtypes(include=['float64', 'int64']):
  means[col] = np.mean(Auto[col])
means

# Desvio padrão de cada coluna quantitativa
dps = {}
for col in Auto.select_dtypes(include=['float64', 'int64']):
  dps[col] = np.std(Auto[col])
dps

